function [V,W,invHi,sigma] = AdditiveVankaSetup(h,gppw,gamma,smoother,stencil)

    omega = (2 * pi)/(h * gppw);
    sigma = omega^2 * (1 - gamma/omega * 1i);
    s = (h^2) * sigma;

    if smoother == "ElementVanka"
        V = [1 1 1 1]';
        W = (1/4) * eye(4);
        if stencil == 9

            Ai = (1/(6*h^2))*[20, -4, -4, -1 ;...
                              -4, 20, -1, -4 ;...
                              -4, -1, 20, -4 ;...
                              -1, -4, -4, 20];

            Mi = (sigma/12)*[8, 1, 1, 0 ;...
                             1, 8, 0, 1 ;...
                             1, 0, 8, 1 ;...
                             0, 1, 1, 8];
            Hi = Ai - Mi;
        elseif stencil == 5
            Hi = (1/h^2)*[4-s, -1, -1, 0 ;...
                          -1, 4-s, 0, -1 ;...
                          -1, 0, 4-s, -1 ;...
                          0, -1, -1, 4-s];
        end
    elseif smoother == "FullVanka"
        if stencil == 9
            V = ones(9,1);
            W = (1/9) * eye(9);

            Ai = (1/(6*h^2))*[20  -4   0  -4  -1   0   0   0   0; ...
                              -4  20  -4  -1  -4  -1   0   0   0; ...
                               0  -4  20   0  -1  -4   0   0   0; ...
                              -4  -1   0  20  -4   0  -4  -1   0; ...
                              -1  -4  -1  -4  20  -4  -1  -4  -1; ...
                               0  -1  -4   0  -4  20   0  -1  -4; ...
                               0   0   0  -4  -1   0  20  -4   0; ...
                               0   0   0  -1  -4  -1  -4  20  -4; ...
                               0   0   0   0  -1  -4   0  -4  20;];

            Mi = (sigma/12)*[8 1 0 1 0 0 0 0 0; ...
                             1 8 1 0 1 0 0 0 0; ...
                             0 1 8 0 0 1 0 0 0; ...
                             1 0 0 8 1 0 1 0 0; ...
                             0 1 0 1 8 1 0 1 0; ...
                             0 0 1 0 1 8 0 0 1; ...
                             0 0 0 1 0 0 8 1 0; ...
                             0 0 0 0 1 0 1 8 1; ...
                             0 0 0 0 0 1 0 1 8; ];
            Hi = Ai - Mi;
        elseif stencil == 5 % coincidies with PlusVanka
            V = [1 1 1 1 1]';
            W = (1/5) * eye(5); 

            Ai = (1/h^2)*[ 4,  0,  -1,  0,  0 ;...
                           0,  4,  -1,  0,  0 ;...
                          -1, -1,   4, -1, -1 ;...
                           0,  0,  -1,  4,  0 ;...
                           0,  0,  -1,  0,  4];

            Mi = sigma * eye(5);
            Hi = Ai - Mi;

        end 

    elseif smoother == "RBVanka"
        if stencil == 9
            V = ones(5,1);
            W = (1/5) * eye(5);

            Ai = (1/(6*h^2))*[20  0 -1  0  0 ; ...
                               0 20 -1  0  0 ; ...
                              -1 -1 20 -1 -1 ; ...
                               0  0 -1 20  0 ; ...
                               0  0 -1  0 20]; ...


            Mi = sigma * (2/3) * eye(5);
            
            Hi = Ai - Mi;
        end

     elseif smoother == "PlusVanka"
        if stencil == 9
            V = ones(5,1);
            W = (1/5) * eye(5);

            Ai = (1/(6*h^2))*[ 20  -1  -4  -1   0; ...
                               -1  20  -4   0  -1; ...
                               -4  -4  20  -4  -4; ...
                               -1   0  -4  20  -1; ...
                                0  -1  -4  -1  20];

            Mi = (sigma/12)*[8 0 1 0 0; ...
                             0 8 1 0 0; ...
                             1 1 8 1 1; ...
                             0 0 1 8 0; ...
                             0 0 1 0 8];
            Hi = Ai - Mi;
        elseif stencil == 5
            V = [1 1 1 1 1]';
            W = (1/5) * eye(5); 

            Ai = (1/h^2)*[ 4,  0,  -1,  0,  0 ;...
                           0,  4,  -1,  0,  0 ;...
                          -1, -1,   4, -1, -1 ;...
                           0,  0,  -1,  4,  0 ;...
                           0,  0,  -1,  0,  4];

            Mi = sigma * eye(5);
            Hi = Ai - Mi;

        end 

    elseif smoother == "Jacobi"
        V = 1;
        W = 1;
        if stencil == 5
            Hi = (1/h^2)*4 - sigma;
        elseif stencil == 9
            Hi = (1/h^2)*10/3 - (2/3)*sigma;
        end
    end
    
    invHi = inv(Hi);

end